psqi['comp1'].value_counts()

"""## COMP 2: Sleep Latency"""

comp2a = []

for row in psqi['psqi_tiempo_para_dormirse']:
  if row <= 15:
    comp2a.append(0)
  elif row >= 16 and row <= 30:
    comp2a.append(1)
  elif row >= 31 and row <= 60:
    comp2a.append(2)
  elif row > 60:
    comp2a.append(3)


psqi['comp2a'] = comp2a

